# Пайдаланушыдан балл енгізуді сұраймыз
ball = int(input("Балл енгізіңіз (0-100): "))

# Баға қою шарттары
if 90 <= ball <= 100:
    print("Өте жақсы")
elif 70 <= ball <= 89:
    print("Жақсы")
elif 50 <= ball <= 69:
    print("Қанағаттанарлық")
elif 0 <= ball <= 49:
    print("Қанағаттанарлықсыз")
else:
    print("Қате! 0 мен 100 аралығында балл енгізіңіз.")

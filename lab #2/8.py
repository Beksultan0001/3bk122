# Пайдаланушыдан үш бүтін санды енгізуді сұраймыз
a = int(input("Бірінші санды енгізіңіз: "))
b = int(input("Екінші санды енгізіңіз: "))
c = int(input("Үшінші санды енгізіңіз: "))

# Шарттарды тексереміз
if a < b < c:
    print("Сандар өсу ретімен орналасқан.")
elif a > b > c:
    print("Сандар кему ретімен орналасқан.")
else:
    print("Сандар ретсіз орналасқан.")
